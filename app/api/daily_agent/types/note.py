from ..base import BaseAgent


class NoteAgent(BaseAgent):
    def get_instruction(self) -> str:
        return """
- 목적: 입력 메모를 '노트(note)' 형식의 마크다운 문서로 구조화합니다.
- 입력에 없는 배경지식/정의/예시/근거를 새로 만들지 마세요.
- 구조는 다음을 기본으로 합니다:
  1) 상위 주제: 1., 2., 3.
  2) 소주제: (1), (2), (3)
  3) 소주제 아래 내용: 서술형 문장/문단으로 정리
- '-' 불릿은 금지하지 않습니다. 다만 아래 경우에만 선택적으로 사용하세요:
  - 중요한 요점/결론/주의사항을 짧게 강조할 때
  - 체크리스트/항목 나열이 더 자연스러울 때
- 정보가 부족한 항목은 '미기재'로 둡니다.
"""

    def get_few_shot(self) -> str:
        return """
[입력 예시]
"JWT 만료 처리 헷갈림. refresh token 필요 이유 정리해야 함"

[출력 예시]
{
  "title": "JWT 만료 처리와 Refresh Token 필요성 정리",
  "content": "## 핵심\n1. JWT 만료 처리\n(1) 만료 처리 방식이 헷갈린다. 어떤 상황에서 토큰 재발급이 필요한지 흐름을 정리해야 한다.\n\n2. Refresh Token\n(1) Refresh Token을 왜 사용하는지 이유를 정리해야 한다. Access Token 만료와 어떤 식으로 역할이 분리되는지 메모가 필요하다.\n\n## 상세 노트\n1. 현재 메모에 포함된 내용\n(1) JWT 만료 처리\n이 메모는 만료 처리 흐름이 정리되지 않았다는 문제를 중심으로 작성되어 있다.\n\n(2) Refresh Token 필요 이유\nRefresh Token의 필요성을 설명할 근거/정리 포인트가 메모에 남아 있다.\n\n- 중요: 현재 메모에는 구체적인 정책/시간/구현 방식은 미기재 상태다.\n",
  "keywords": ["JWT", "만료", "Refresh Token"]
}
"""

    def get_template(self) -> str:
        return """
## 핵심
1. {대주제}
(1) {소주제}
{서술형 정리 문장(문단)}

2. {대주제}
(1) {소주제}
{서술형 정리 문장(문단)}

## 상세 노트
1. {대주제}
(1) {소주제}
{서술형 정리 문장(문단)}

- {정말 중요한 요점이 있을 때만 불릿 사용}
"""
