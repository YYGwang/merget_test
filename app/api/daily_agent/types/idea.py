# app/api/daily_agent/types/idea.py
from ..base import BaseAgent

class IdeaAgent(BaseAgent):
    def get_few_shot(self) -> str:
        return """
[예시 1: 서비스 기획]
입력: "사람들이 남긴 메모를 자동으로 일기로 바꿔주는 앱 있으면 어떨까? 매일 밤에 푸시 알림 와서 그날 메모 보여주고."
결과: ### 💡 아이디어
- **주제**: 메모 기반 자동 일기 생성 서비스
- **핵심 내용**: 파편화된 메모 데이터를 시간순으로 결합하여 하루의 서사를 완성하는 저널링 도구
- **보완/확장 생각**: 사용자의 기분을 분석하는 감정 태그 기능이나 사진 데이터 결합 방안 검토 필요

[예시 2: 서울 주거 프로젝트 확장]
입력: "주거 프로젝트에 지하철 혼잡도 데이터도 넣으면 좋을 듯. 출퇴근 시간 고려해서 집 추천해 주게."
결과: ### 💡 아이디어
- **주제**: 서울 주거 추천 알고리즘 고도화
- **핵심 내용**: 단순 통근 시간 외에 '이동의 질(혼잡도)'을 지표로 추가
- **보완/확장 생각**: 공공데이터 포털의 실시간 지하철 혼잡도 API 활용 가능성 확인
"""

    def get_template(self) -> str:
        return """### 💡 아이디어
- **주제**: {아이디어 제목}
- **핵심 내용**: {생각의 구체적 설명}
- **보완/확장 생각**: {더 고민해볼 지점}"""