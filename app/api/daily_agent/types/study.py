from ..base import BaseAgent

class StudyAgent(BaseAgent):
    def get_instruction(self) -> str:
        """
        Study 노드 전용 상세 지침:
        메서드 명칭을 base.py의 추상 메서드와 일치시켰습니다.
        """
        return """
        - 당신은 복잡한 개념을 논리적 구조로 분해하는 '학습 조력자'입니다.
        - 파편화된 정보를 주제별로 그룹화하여 '내용 정리' 섹션에 담으세요.
        - 전문 용어는 유지하되 설명은 친절하게 복원하세요.
        - 제목(title)은 전체 내용을 관통하는 학술적이고 구체적인 문장으로 작성하세요.
        -짧은 글이 들어와도 없는 내용 만들지마
        """

    def get_few_shot(self) -> str:
        """호날두 테스트 케이스를 기반으로 한 Few-shot입니다."""
        return """
[입력 예시]
"호날두는 가난한 집안에서 태어나... (중략) ... 결국 성공했다."

[출력 결과]
{
    "title": "크리스티아누 호날두의 역경 극복과 성공적인 프로 입문 과정",
    "content": "1. 내용 정리\\n- 성장 배경: 포르투갈 마데이라 섬의 빈곤층 가문 출신으로 알코올/마약 중독 등 불안정한 환경을 극복함\\n- 재능 발견: 왜소한 체격에도 불구하고 독보적인 기술로 12세에 명문 스포르팅 리스본 입단\\n- 위기 극복: 타지 생활의 차별을 견디며 철저한 자기 관리와 훈련으로 성공의 발판 마련\\n\\n2. 요약\\n- 열악한 환경적 제약을 압도적인 노력과 피나는 훈련으로 정면 돌파하며 세계적인 선수의 기반을 닦음"
}
"""

    def get_template(self) -> str:
        """사용자 요청 반영: # 제거, 일자 제거, 2단 구조(내용 정리, 요약)"""
        return """
1. 내용 정리
- (핵심 주제 1): (메모 속 정보를 성격에 따라 분류하여 논리적 문장으로 복원)
- (핵심 주제 2): (메모 속 정보를 성격에 따라 분류하여 논리적 문장으로 복원)
- (핵심 주제 3): (필요 시 추가)

2. 요약
- 전체 내용을 관통하는 핵심 가치나 결론을 한 문장으로 명확하게 정리
"""