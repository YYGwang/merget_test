from ..base import BaseAgent


class MeetingAgent(BaseAgent):
    def get_instruction(self) -> str:
        return """
- 목적: 회의 기록을 '회의록' 형태로 구조화합니다.
- 입력에 없는 참석자/시간/결정사항/액션아이템은 절대 지어내지 마세요.
- 없으면 '미기재'로 표기하고, 확인 가능한 내용만 추출하세요.
"""

    def get_few_shot(self) -> str:
        return """
[입력 예시]
"온라인 판매 점검 회의. A사 입점 완료, B사는 6월말까지. 백과장 5/19까지 성과 지표 보고."

[출력 예시]
{
  "title": "온라인 판매 점검 회의 정리",
  "content": "## 회의 개요
- 일시: 미기재
- 참석자: 미기재
- 주제/안건: 온라인 판매 점검

## 논의 내용
1. A사 입점 현황
  1-1. A사 입점 완료
2. B사 입점 계획
  2-1. 6월 말까지 진행

## 결정사항
- 미기재

## 액션 아이템
1. 성과 지표 보고
  1-1. 담당: 백과장
  1-2. 기한: 5/19",
  "keywords": ["온라인 판매", "입점", "성과 지표"]
}
"""

    def get_template(self) -> str:
        return """
## 회의 개요
- 일시: {일시 또는 미기재}
- 참석자: {참석자 또는 미기재}
- 주제/안건: {주제 또는 미기재}

## 논의 내용
1. {주제}
  1-1. {세부}
2. {주제}
  2-1. {세부}

## 결정사항
- {결정사항 또는 미기재}

## 액션 아이템
1. {할 일}
  1-1. 담당: {담당자 또는 미기재}
  1-2. 기한: {기한 또는 미기재}
"""
