from ..base import BaseAgent


class MeetingAgent(BaseAgent):
    def get_instruction(self) -> str:
        return """
- 목적: 입력 메모를 '회의록' 형식의 마크다운 문서로 구조화합니다.
- 입력에 없는 참석자/시간/결정사항/액션 아이템을 절대 지어내지 마세요.
- 확인 가능한 정보만 추출하고, 없으면 반드시 '미기재'로 표기하세요.

[표현 규칙]
- 큰 구조는 번호로 작성합니다: 1., 2., 3.
- 번호 아래에 소주제가 필요하면 괄호 번호로 작성합니다: (1), (2), (3)
- 소주제 아래의 내용은 기본적으로 서술형 문장/문단으로 정리합니다.
- '-' 불릿은 금지하지 않습니다. 아래 경우에만 선택적으로 사용하세요:
  - 회의 개요(일시/참석자/안건)처럼 필드 나열이 필요한 경우
  - 액션 아이템에서 담당/기한 등 속성 나열이 필요한 경우
  - 정말 중요한 요점/결론/주의사항을 짧게 강조할 때
"""

    def get_few_shot(self) -> str:
        return """
[입력 예시]
"온라인 판매 점검 회의. A사 입점 완료, B사는 6월 말까지. 백과장 5/19까지 성과 지표 보고."

[출력 예시]
{
  "title": "온라인 판매 점검 회의 정리",
  "content": "## 회의 개요\n- 일시: 미기재\n- 참석자: 미기재\n- 주제/안건: 온라인 판매 점검\n\n## 논의 내용\n1. 입점 현황\n(1) A사\nA사 입점이 완료되었다.\n\n(2) B사\nB사는 6월 말까지 입점을 진행하기로 언급되었다.\n\n## 결정사항\n- 미기재\n\n## 액션 아이템\n1. 성과 지표 보고\n(1) 담당/기한\n- 담당: 백과장\n- 기한: 5/19\n"  
}
"""

    def get_template(self) -> str:
        return """
## 회의 개요
- 일시: {일시 또는 미기재}
- 참석자: {참석자 또는 미기재}
- 주제/안건: {주제 또는 미기재}

## 논의 내용
1. {논의 대주제}
(1) {소주제}
{서술형 정리 문장(문단)}

## 결정사항
- {결정사항 또는 미기재}

## 액션 아이템
1. {할 일}
(1) 담당/기한
- 담당: {담당자 또는 미기재}
- 기한: {기한 또는 미기재}
"""
