from ..base import BaseAgent


class MeetingAgent(BaseAgent):
    def get_instruction(self) -> str:
        return """
- 목적: 입력 메모를 '회의록' 형식의 마크다운 문서로 구조화합니다.
- 입력에 없는 참석자/시간/결정사항/액션 아이템을 절대 지어내지 마세요.
- 확인 가능한 정보만 추출하고, 없으면 반드시 '미기재'로 표기하세요.
- 상위 항목만 번호(1., 2., 3.)를 사용하세요.
- 하위 항목에는 숫자 번호를 절대 사용하지 마세요. (1-1, 2-1 금지)
- 하위 항목은 반드시 '-' 불릿으로 작성하세요.
"""

    def get_few_shot(self) -> str:
        return """
[입력 예시]
"온라인 판매 점검 회의. A사 입점 완료, B사는 6월 말까지. 백과장 5/19까지 성과 지표 보고."

[출력 예시]
{
  "title": "온라인 판매 점검 회의 정리",
  "content": "## 회의 개요
- 일시: 미기재
- 참석자: 미기재
- 주제/안건: 온라인 판매 점검

## 논의 내용
1. A사 입점 현황
- A사 입점 완료
2. B사 입점 계획
- 6월 말까지 진행

## 결정사항
- 미기재

## 액션 아이템
1. 성과 지표 보고
- 담당: 백과장
- 기한: 5/19",
  "keywords": ["온라인 판매", "입점", "성과 지표"]
}
"""

    def get_template(self) -> str:
        return """
## 회의 개요
- 일시: {일시 또는 미기재}
- 참석자: {참석자 또는 미기재}
- 주제/안건: {주제 또는 미기재}

## 논의 내용
1. {논의 주제}
- {세부 내용}

## 결정사항
- {결정사항 또는 미기재}

## 액션 아이템
1. {할 일}
- 담당: {담당자 또는 미기재}
- 기한: {기한 또는 미기재}
"""
