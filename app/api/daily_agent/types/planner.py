from ..base import BaseAgent


class PlannerAgent(BaseAgent):
    def get_instruction(self) -> str:
        return """
- 목적: 해야 할 일/일정/마감을 'Planner' 형식으로 구조화합니다.
- 입력에 없는 기한/우선순위/담당자를 절대 지어내지 마세요.
- 확인 가능한 정보만 추출하고, 없으면 반드시 '미기재'로 표기하세요.

[표현 규칙]
- 큰 구조는 번호로 작성합니다: 1., 2., 3.
- 할 일은 괄호 번호로 작성합니다: (1), (2), (3)
- (n) 아래의 설명은 기본적으로 서술형으로 짧게 정리할 수 있습니다.
- 기한/우선순위/담당자 같은 '속성'은 가독성을 위해 '-' 불릿으로 나열해도 됩니다.
- '-' 불릿은 금지하지 않지만, 불필요한 나열로 남발하지 마세요.
"""

    def get_few_shot(self) -> str:
        return """
[입력 예시]
"내일 아침 9시에 토익 접수. 이번주 내로 방 청소"

[출력 예시]
{
  "title": "개인 할 일 및 기한 정리",
  "content": "## Planner\n1. 할 일 목록\n(1) 토익 접수\n내일 아침 9시에 접수해야 한다.\n- 기한: 내일 09:00\n- 우선순위: 미기재\n- 담당: 미기재\n\n(2) 방 청소\n이번 주 내로 방 청소를 해야 한다.\n- 기한: 이번 주 내\n- 우선순위: 미기재\n- 담당: 미기재\n\n## 비고\n- 미기재\n",
  "keywords": ["토익", "접수", "청소"]
}
"""

    def get_template(self) -> str:
        return """
## Planner
1. 할 일 목록
(1) {할 일}
{서술형 정리 문장(짧게)}

- 기한: {기한 또는 미기재}
- 우선순위: {우선순위 또는 미기재}
- 담당: {담당자 또는 미기재}

## 비고
- {추가 메모 또는 미기재}
"""
